import { useState } from "react";

const Section = ({
  title,
  description,
  isVisible,
  setIsVisible,
  setVisibleSection,
}) => {
  return (
    <div className="border border-black p-2 m-2">
      <h3 className="font-bold text-xl">{title}</h3>
      {isVisible ? (
        <>
          <button
            onClick={() => {
              setIsVisible(false);
              setVisibleSection();
            }}
            className="cursor-pointer underline"
          >
            Hide
          </button>
          <p className="m-2">{description}</p>
        </>
      ) : (
        <button
          onClick={() => setIsVisible(true)}
          className="cursor-pointer underline"
        >
          Show
        </button>
      )}
    </div>
  );
};

